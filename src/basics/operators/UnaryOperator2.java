package basics.operators;

public class UnaryOperator2 {
    public static void main(String[] args) {
        int x = 5;
        int y = -5;
        boolean a = true;
        boolean b = false;
        System.out.println(~x);
        System.out.println(~y);
        System.out.println(!a);
        System.out.println(!b);
    }
}
