package basics.operators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 4;
        int b = 9;
        System.out.println("a+b = " + (a+b));
        System.out.println("a-b = " + (a-b));
        System.out.println("a*b = " + (a*b));
        System.out.println("a/b = " + (a/b));
        System.out.println("a%b = " + (a%b));
    }
}
